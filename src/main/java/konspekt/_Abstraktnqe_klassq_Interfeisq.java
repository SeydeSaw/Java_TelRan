package konspekt;

public class _Abstraktnqe_klassq_Interfeisq {
    /*
    todo Абстрактные классы:
    Это классы, которые могут содержать как обычные, так и абстрактные методы.
Ограничения: Класс может наследоваться только от одного абстрактного класса.
Использование: Когда у вас есть общая логика между подклассами и вы хотите предоставить некоторую реализацию.

    Abstrakte Klassen:
Das sind Klassen, die sowohl normale als auch abstrakte Methoden enthalten können.
Beschränkungen: Eine Klasse kann nur von einer abstrakten Klasse erben.
Verwendung: Wenn es eine gemeinsame Logik zwischen Unterklassen gibt und Sie eine bestimmte Implementierung bereitstellen möchten.

    Abstract Classes:
These are classes that can contain both regular and abstract methods.
Limitations: A class can only inherit from one abstract class.
Use: When you have common logic among subclasses and you want to provide some implementation.

    todo Интерфейсы:
    Это контракт, который класс обещает выполнить. Интерфейсы содержат только абстрактные методы (без тела).
Ограничения: Класс может реализовывать несколько интерфейсов.
Использование: Когда вы хотите, чтобы несколько классов реализовывали один и тот же контракт с различной логикой.

    Schnittstellen:
Das ist ein Vertrag, den eine Klasse verspricht zu erfüllen. Schnittstellen enthalten nur abstrakte Methoden (ohne Körper).
Beschränkungen: Eine Klasse kann mehrere Schnittstellen implementieren.
Verwendung: Wenn Sie möchten, dass mehrere Klassen denselben Vertrag mit unterschiedlicher Logik erfüllen.

    Interfaces:
This is a contract that a class promises to fulfill. Interfaces contain only abstract methods (without bodies).
Limitations: A class can implement multiple interfaces.
Use: When you want several classes to implement the same contract with different logic.

    todo Совет:
Используйте абстрактные классы, если у вас есть общая логика и часть реализации между классами.
Используйте интерфейсы, если вам нужен чистый контракт и вы хотите, чтобы различные классы предоставляли
свою собственную логику для этого контракта.
Интерфейсы предоставляют большую гибкость, так как классы могут реализовывать несколько интерфейсов.

    Rat:
Verwenden Sie abstrakte Klassen, wenn Sie gemeinsame Logik und einen Teil der Implementierung zwischen den Klassen haben.
Verwenden Sie Schnittstellen, wenn Sie einen reinen Vertrag benötigen und möchten, dass verschiedene Klassen
ihre eigene Logik für diesen Vertrag bereitstellen.
Schnittstellen bieten mehr Flexibilität, da Klassen mehrere Schnittstellen implementieren können.

    Advice:
Use abstract classes if you have common logic and part of the implementation among classes.
Use interfaces if you need a pure contract, and you want different classes to provide
their own logic for that contract.
Interfaces provide greater flexibility since classes can implement multiple interfaces.
     */
}
