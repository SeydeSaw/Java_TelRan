public class _Modifikatorq_Dostupa {
    /*
    В Java есть четыре основных модификатора доступа, контролирующих видимость классов, полей, методов и
    других элементов в программе. Вот краткое описание каждого:

    In Java gibt es vier grundlegende Zugriffsmodifikatoren, die die Sichtbarkeit von Klassen, Feldern, Methoden und
    anderen Elementen im Programm steuern. Hier ist eine kurze Beschreibung von jedem:

    public (открытый):
    Описание: Элемент с модификатором public доступен из любого другого класса.
    Применение: Используется, когда нужно, чтобы член был доступен из любой части программы.

    public (öffentlich):
    Beschreibung: Ein Element mit dem Zugriffsmodifikator "public" ist von jeder anderen Klasse aus zugänglich.
    Anwendung: Wird verwendet, wenn ein Mitglied von jedem Teil des Programms aus zugänglich sein soll.

    private (закрытый):
    Описание: Элемент с модификатором private доступен только внутри своего собственного класса.
    Применение: Используется, когда нужно ограничить доступ и изменение члена только внутри самого класса.

    private (privat):
    Beschreibung: Ein Element mit dem Zugriffsmodifikator "private" ist nur innerhalb seiner eigenen Klasse zugänglich.
    Anwendung: Wird verwendet, um den Zugriff und die Änderung eines Elements nur innerhalb der Klasse zu beschränken.

    protected (защищенный):
    Описание: Элемент с модификатором protected доступен внутри своего собственного пакета и для подклассов,
    даже если они находятся в других пакетах.
    Применение: Часто используется в наследовании для предоставления доступа подклассам.

    protected (geschützt):
    Beschreibung: Ein Element mit dem Zugriffsmodifikator "protected" ist innerhalb seines eigenen Pakets und
    für Unterklassen zugänglich, auch wenn sie sich in anderen Paketen befinden.
    Anwendung: Wird häufig bei Vererbung verwendet, um den Unterklassen Zugriff zu gewähren.

    default (по умолчанию):
    Описание: Если модификатор доступа не указан, то элемент имеет "пакетный" доступ (или "по умолчанию"),
    что означает доступ внутри своего собственного пакета.
    Применение: Этот модификатор часто используется, когда элементы класса предназначены для использования
    только внутри того же пакета.

    default (Standard):
    Beschreibung: Wenn kein Zugriffsmodifikator angegeben ist, hat das Element "paketweiten" Zugriff
    (oder "Standardzugriff"), was bedeutet, dass es innerhalb seines eigenen Pakets zugänglich ist.
    Anwendung: Dieser Modifikator wird oft verwendet, wenn die Elemente einer Klasse nur
    innerhalb desselben Pakets verwendet werden sollen.
     */
}