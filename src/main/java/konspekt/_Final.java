package konspekt;

public class _Final {
    /*
    final: Это ключевое слово, которое указывает, что однажды установленное значение остается постоянным :

    - Класс не может быть наследником
    - Метод не может быть переопределен в классах наследниках
    - Поле не может изменить своё значение после инициализации
    - Параметры методов не могут изменять своё значение внутри метода
    - Локальные переменные не могут быть изменены после присвоения им значения

    final: Dies ist ein Schlüsselwort, das angibt, dass einmal zugewiesene Werte konstant bleiben:

    Die Klasse kann nicht vererbt werden.
    Die Methode kann in abgeleiteten Klassen nicht überschrieben werden.
    Das Feld kann seinen Wert nach der Initialisierung nicht ändern.
    Methodenparameter können innerhalb der Methode ihren Wert nicht ändern.
    Lokale Variablen können nach der Zuweisung ihres Wertes nicht geändert werden.
     */
}
