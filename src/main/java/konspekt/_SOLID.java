public class _SOLID {
    /*
    SOLID — это аббревиатура, представленная пятью основными принципами объектно-ориентированного программирования
    и дизайна, которые помогают создавать более гибкие, поддерживаемые и расширяемые кодовые базы.

    SOLID ist eine Abkürzung, die für fünf grundlegende Prinzipien der objektorientierten Programmierung und
    des Designs steht. Diese Prinzipien helfen dabei, flexiblere, wartbare und erweiterbare Code-Basen zu erstellen.

    Вот эти принципы:
    Hier sind diese Prinzipien:

	1. Принцип единственной ответственности (Single Responsibility Principle - SRP):
    Определение: Класс должен иметь только одну причину для изменения.
    Идея: Каждый класс должен быть ответственен только за одну часть функциональности программы.

    1. Prinzip der Einzelverantwortlichkeit (Single Responsibility Principle - SRP):
    Definition: Eine Klasse sollte nur einen Grund für eine Änderung haben.
    Idee: Jede Klasse sollte nur für einen Teil der Programmfunktionalität verantwortlich sein.

	2. Принцип открытости/закрытости (Open/Closed Principle - OCP):
    Определение: Программные сущности (классы, модули, функции и т.д.) должны быть открыты для расширения,
    но закрыты для модификации.
    Идея: Используйте наследование и интерфейсы для добавления новой функциональности, не изменяя существующий код.

    2. Offen-Geschlossen-Prinzip (Open/Closed Principle - OCP):
    Definition: Programm-Entitäten (Klassen, Module, Funktionen usw.) sollten für Erweiterungen offen,
    aber für Modifikationen geschlossen sein.
    Idee: Verwende Vererbung und Schnittstellen, um neue Funktionalitäten hinzuzufügen, ohne den bestehenden Code zu ändern.

	3. Принцип подстановки Барбары Лисков (Liskov Substitution Principle - LSP):
    Определение: Объекты базового класса могут быть заменены объектами производного класса
    без изменения корректности программы.
    Идея: Производные классы должны дополнять, а не изменять базовые классы.

    3. Liskovsches Substitutionsprinzip (Liskov Substitution Principle - LSP):
    Definition: Objekte der Basisklasse können durch Objekte der abgeleiteten Klasse ersetzt werden,
    ohne die Korrektheit des Programms zu beeinträchtigen.
    Idee: Abgeleitete Klassen sollten die Basisklassen ergänzen, nicht ändern.

	4. Принцип разделения интерфейса (Interface Segregation Principle - ISP):
    Определение: Клиенты не должны зависеть от интерфейсов, которые они не используют.
    Идея: Создавайте маленькие, специфичные интерфейсы, чтобы избежать создания толстых интерфейсов,
    которые не все клиенты будут использовать.

    4. Prinzip der Schnittstellensegregation (Interface Segregation Principle - ISP):
    Definition: Clients sollten nicht von Schnittstellen abhängig sein, die sie nicht nutzen.
    Idee: Erstelle kleine, spezifische Schnittstellen, um das Erstellen von umfangreichen Schnittstellen zu vermeiden,
    die nicht alle Clients verwenden.

	5. Принцип инверсии зависимостей (Dependency Inversion Principle - DIP):
    Определение: Модули верхнего уровня не должны зависеть от модулей нижнего уровня.
    Оба типа модулей должны зависеть от абстракций.
    Абстракции не должны зависеть от деталей. Детали должны зависеть от абстракций.
    Идея: Зависимости в коде должны строиться вокруг абстракций, а не вокруг конкретных реализаций.

    5. Prinzip der Umkehrung der Abhängigkeiten (Dependency Inversion Principle - DIP):
    Definition: Obere Module sollten nicht von unteren Modulen abhängig sein.
    Beide Arten von Modulen sollten von Abstraktionen abhängen. Abstraktionen sollten nicht von Details abhängen.
    Details sollten von Abstraktionen abhängen.
    Idee: Abhängigkeiten im Code sollten sich um Abstraktionen, nicht um konkrete Implementierungen, gruppieren.
     */
}