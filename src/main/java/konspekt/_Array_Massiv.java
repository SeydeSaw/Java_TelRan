package konspekt;
/*
    Массив - коллекция элементов определенного типа данных, которая имеет фиксированную длину.
    Массивы позволяют хранить несколько элементов одного типа в одной переменной.

    Array - eine Sammlung von Elementen eines bestimmten Datentyps mit fester Länge.
    Arrays ermöglichen es, mehrere Elemente eines Typs in einer Variablen zu speichern.

    Основные особенности массивов включают:
    Die Hauptmerkmale von Arrays sind:

    Фиксированная/Динамическая длина:
    При создании массива необходимо указать его длину, которая определяется количеством элементов,
    которые можно хранить в массиве. Длина массива остается неизменной после создания,
    и нельзя добавлять или удалять элементы из массива.

    Feste/Dynamische Länge:
    Beim Erstellen eines Arrays muss seine Länge angegeben werden, die durch die Anzahl der Elemente bestimmt wird,
    die im Array gespeichert werden können. Die Länge des Arrays bleibt nach der Erstellung unverändert,
    und es können keine Elemente hinzugefügt oder entfernt werden.

    Индексирование:
    Элементы массива индексируются, начиная с нуля.
    Индекс используется для доступа к определенному элементу массива.

    Indizierung:
    Die Elemente des Arrays werden ab Null indiziert.
    Der Index wird verwendet, um auf ein bestimmtes Arrayelement zuzugreifen.

    Однородность типов:
    Тип элементов массива должен быть определен при его создании, и все элементы в массиве
    должны быть совместимы с этим типом.

    Homogenität der Typen:
    Der Datentyp der Arrayelemente muss bei seiner Erstellung festgelegt werden,
    und alle Elemente im Array müssen mit diesem Typ kompatibel sein.

    Итерация:
    Массивы могут быть перебраны с помощью циклов, таких как цикл for или цикл foreach,
    для обработки всех элементов массива.

    Iteration:
    Arrays können mithilfe von Schleifen wie der for-Schleife oder der foreach-Schleife durchlaufen werden,
    um alle Elemente des Arrays zu verarbeiten.

    Память:
    Элементы массива хранятся в памяти непосредственно друг за другом.
    Это обеспечивает эффективный доступ к элементам массива по индексу.

    Speicher:
    Die Arrayelemente werden direkt hintereinander im Speicher gespeichert.
    Dies gewährleistet einen effizienten Zugriff auf die Arrayelemente über ihren Index.

    Многомерные массивы:
    позволяют создавать массивы с несколькими измерениями, такими как двумерные, трехмерные и т.д.
    Они представляют собой матрицу из элементов.

    Mehrdimensionale Arrays:
    Ermöglichen die Erstellung von Arrays mit mehreren Dimensionen, wie z. B. zweidimensionalen, dreidimensionalen usw.
    Sie repräsentieren eine Matrix von Elementen.

 */
